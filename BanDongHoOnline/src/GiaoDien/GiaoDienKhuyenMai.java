        txtMaKM.setText((String) jTable1.getValueAt(row,1));
        txtMaNV.setText((String) jTable1.getValueAt(row,2));
        txtLyDo.setText((String) jTable1.getValueAt(row,3));
        String datebd =  jTable1.getValueAt(jTable1.getSelectedRow(), 4).toString(); 
        String datekt =  jTable1.getValueAt(jTable1.getSelectedRow(), 5).toString();
        datebd=datebd.substring(8,10)+"/"+datebd.substring(5,7)+"/"+datebd.substring(0,4);
        datekt=datekt.substring(8,10)+"/"+datekt.substring(5,7)+"/"+datekt.substring(0,4);

        try {
            java.util.Date  datetmp = new SimpleDateFormat("dd/MM/yyyy").parse(datebd);
             dateNgayBD.setDate(datetmp);
             java.util.Date  datetmp1 = new SimpleDateFormat("dd/MM/yyyy").parse(datekt);
             dateNgayKT.setDate(datetmp1);
         } catch (ParseException ex) {
             Logger.getLogger(GiaoDienKhuyenMai.class.getName()).log(Level.SEVERE, null, ex);
         }   
        if(evt.getClickCount()==2){
            jTable2.setDefaultEditor(Object.class, null);
            //jTable1.setSelectionBackground(Color.RED);
            jTable2.setSelectionMode(0);
              showCTKM();
         }
        
