    private void btnThem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThem1ActionPerformed
       
        if(txtMaKM.getText().equals("") || txtMaNV.getText().equals("") || txtLyDo.getText().equals("") ||dateNgayBD.getDate().equals("") || dateNgayKT.getDate().equals(""))
                 baoloi.setText("Vui lòng nhập đầy đủ thông tin khuyến mãi!"); 
        else{
            
            int row = jTable1.getSelectedRow();
            KhuyenMai km = new KhuyenMai(txtMaKM.getText(), txtMaNV.getText(), txtLyDo.getText(), dateNgayBD.getDate(),dateNgayKT.getDate());
           if (jTable1.getSelectedRow()==-1){
            
                for (KhuyenMai kmm : Controller.controller.arrayListKhuyenMai)
                    if(kmm.getMaKM().equals(txtMaKM.getText())){
                        baoloi.setText("Mã khuyến mãi đã tồn tại!");
                        return;
                    }
                Connection.InsertData.insertKhuyenMai(km);
                
                showData0();


                jTable1.getSelectionModel().setSelectionInterval(jTable1.getRowCount()-1,jTable1.getRowCount()-1);

             }
            else
           {
                
                Connection.UpdateData.updateKhuyenMai(km);
               
           }
           showData0();
            //showData();
           jTable1.getSelectionModel().setSelectionInterval(row, row);
        }          // TODO add your handling code here:
    }//GEN-LAST:event_btnThem1ActionPerformed

